class Profile {
  final String id;
  final String name;
  final String email;
  final String phone;
  final String company;
  final String role;
  final String avatar;

  Profile({
    required this.id,
    required this.name,
    required this.email,
    required this.phone,
    required this.company,
    required this.role,
    required this.avatar,
  });
}